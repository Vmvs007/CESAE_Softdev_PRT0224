package Ex_06;

public class Calculadora {

    public Calculadora() {
    }

    /**
     * Método para calcular a soma de dois números reais
     * @param num1 Numero real
     * @param num2 Numero real
     * @return Soma dos Dois Números Reais
     */
    public double soma(double num1, double num2) {
        return num1 + num2;
    }

    /**
     * Método para calcular a subtracao de dois números reais
     * @param num1 Numero real
     * @param num2 Numero real
     * @return Subtracao dos Dois Números Reais
     */
    public double subtracao(double num1, double num2) {
        return num1 - num2;
    }

    /**
     * Método para calcular a multiplicacao de dois números reais
     * @param num1 Numero real
     * @param num2 Numero real
     * @return Multiplicacao dos Dois Números Reais
     */
    public double multiplicacao(double num1, double num2) {
        return num1 * num2;
    }

    /**
     * Método para calcular a divisao de dois números reais
     * @param num1 Numero real
     * @param num2 Numero real
     * @return Divisao dos Dois Números Reais
     */
    public double divisao(double num1, double num2) {
        return num1 / num2;
    }

}
